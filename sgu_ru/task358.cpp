#include <iostream>
#include <algorithm>

using namespace std;

/**************************************************************************
* GetM()                                                                  *
*                                                                         *
**************************************************************************/
int GetM(int a, int b, int c)
{
   int tmp[3] = { a, b, c };
   std::sort(tmp, tmp + 3);
   return tmp[1];
}

/**************************************************************************
* main()                                                                  *
*                                                                         *
**************************************************************************/
int main(void)
{
   int q, w, e;
   cin >> q >> w >> e;
   int a, s, d;
   cin >> a >> s >> d;
   int z, x, c;
   cin >> z >> x >> c;

   int res = GetM(
      GetM(q, w, e),
      GetM(a, s, d),
      GetM(z, x, c)
      );

   cout << res << endl;

   return 0;
}

